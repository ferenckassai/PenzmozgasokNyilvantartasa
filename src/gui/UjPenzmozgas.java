    public UjPenzmozgas(java.awt.Frame parent, boolean modal, IModel model, Penzmozgas penzmozgas, boolean megse, boolean szerkesztes) {
        super(parent, modal);
        this.model = model;
        this.penzmozgas = penzmozgas;
        this.megse = megse;
        this.szerkesztes = szerkesztes;
        initComponents();

        setLocationRelativeTo(parent);
        if (szerkesztes == false) {
            setTitle("Új pénzmozgás hozzáadása");
        } else {
            setTitle("Pénzmozgás szerkesztése");
        }

        try {
            kategoriak = model.getAllKategoria();
            cbox.setModel(new DefaultComboBoxModel<Kategoria>(kategoriak.toArray(new Kategoria[kategoriak.size()])));

            if (szerkesztes) {
                int katID = penzmozgas.getKategoria();
                Kategoria beir = new Kategoria();
                for (int i = 0; i < kategoriak.size(); i++) {
                    if (katID == kategoriak.get(i).getId()) {
                        beir = kategoriak.get(i);
                    }
                }

                cbox.setSelectedItem(beir);
                tfOsszeg.setText(Integer.toString(penzmozgas.getOsszeg()));
                tfMegjegyzes.setText(penzmozgas.getMegjegyzes());
                tfDatum.setText(penzmozgas.getDatum().toString());
                        
                
                
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(parent, ex, "Adatbázis hiba", JOptionPane.ERROR_MESSAGE);
        }

    }

