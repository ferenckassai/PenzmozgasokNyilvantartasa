    private void jbOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOKActionPerformed
        if (!tfDatum.getText().equals("") || !tfMegjegyzes.getText().equals("")
                || !tfOsszeg.getText().equals("")) {
            megse = false;
            penzmozgas.setDatum((LocalDate.parse(tfDatum.getText())));
            penzmozgas.setOsszeg(Integer.parseInt(tfOsszeg.getText()));
            penzmozgas.setMegjegyzes(tfMegjegyzes.getText());

            Kategoria kat = (Kategoria) cbox.getSelectedItem();

            int id = kat.getId();

            penzmozgas.setIdKategoria(id);

            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Be kéne írni minden adatot!", "Irgumburgum.",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbOKActionPerformed

